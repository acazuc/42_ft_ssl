#include "bignum.h"

uint64_t	bignum_npot(uint64_t val)
{
	val--;
	val |= val >> 1;
	val |= val >> 2;
	val |= val >> 4;
	val |= val >> 8;
	val |= val >> 16;
	val |= val >> 32;
	return (val + 1);
}
